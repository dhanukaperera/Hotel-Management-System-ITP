    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        if(itp.isEmpty(txtMenuPackegeNo, "Menu Package No filed is Empty"))
        {
            if(itp.isEmpty(txtName, "Food Name field is Empty"))
            {
                obj.add("event_menu");
                itp2.MenuNo();
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
         if(itp.isEmpty(txtMenuPackegeNo, "Menu Package No filed is Empty"))
        {
            if(itp.isEmpty(txtItemID, "ItemID field is Empty"))
            {
                if(itp.isEmpty(txtName, "Food Name field is Empty"))
                {
                 int msg = JOptionPane.showConfirmDialog(null, "Do you realy want to Update this record?");
                 if(msg == 0)
                 {
                     obj.update("event_menu");
                 }
                }
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

