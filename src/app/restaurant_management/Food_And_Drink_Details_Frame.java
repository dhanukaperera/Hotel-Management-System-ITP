    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
     if(tblFood_drink_info.getSelectedRow() != -1)
     {
        int msg = JOptionPane.showConfirmDialog(null, "Do you really want to DELETE this record?");
          
            if (msg == 0) 
            {
                  obj.delete("food_drink_info", "FOB ID", obj.getPrimaryKey(),tblFood_drink_info);
                  
                   Dinein.LoadName();
                   restbill.LoadName();
                    roomguest.LoadName();
                
                  obj.tableloadmethod();
            }
            
     }
     else
        JOptionPane.showMessageDialog(null,"Select a row to Delete","Error Message",JOptionPane.ERROR_MESSAGE);

          obj.clear();
    }//GEN-LAST:event_btnDeleteMouseClicked

