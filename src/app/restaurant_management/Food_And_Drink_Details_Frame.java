    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
      if(val.isEmpty(txtName,"Name Can't be Empty")&&(val.isEmpty(txtPrice,"Price Can't be Empty")))
      {
          if((val.isNumber(txtPrice,"Enter a valid Price")==true))
          {
             if(obj.isPositivefloat(txtPrice,"Insert Valid Number")==true)
           {
                if((val.isLetters(txtName,"Enter a valid Name")==true))
                {
                      int msg = JOptionPane.showConfirmDialog(null,"Do you Really want to add the Record?");
                      {
                             if(msg==0)
                                     obj.add("food_drink_info");
                      }
                }
            }
          }
      }
   
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
      if(tblFood_drink_info.getSelectedRow() != -1)
     {
      if(val.isEmpty(txtName,"Name Can't be Empty")&&(val.isEmpty(txtPrice,"Price Can't be Empty")))
      {
          if((val.isNumber(txtPrice,"Enter a valid Price")==true))
          {
           if(obj.isPositivefloat(txtPrice,"Insert Valid Number")==true)
            {
                if((val.isLetters(txtName,"Enter a valid Name")==true))
                {
                  int msg = JOptionPane.showConfirmDialog(null,"Do you really want to UPDATE this record?");

                      if(msg==0)
                      {
                        try
                        {
                            obj.update("food_drink_info");
                        }
                          catch(Exception e)
                          {
                              JOptionPane.showMessageDialog(null,e,"Select the row you want to update",JOptionPane.ERROR_MESSAGE);
                          }
                      }
                  }
            }
         }
      }
     }
      else
        JOptionPane.showMessageDialog(null,"Select a row to Update","Error Message",JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_btnUpdateMouseClicked

