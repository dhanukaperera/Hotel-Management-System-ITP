    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
     if(tblRoomGuestOrders.getSelectedRow() != -1)
     {
            if(val.isEmpty(txtQty,"QTY Can't be Empty"))
            {   
                if((val.isNumber(txtQty,"Enter a valid Quantity")==true))
                { 
                         int msg = JOptionPane.showConfirmDialog(null,"Do you really want to UPDATE this record?");

                         if(msg==0)
                            obj.update("room_guest_orders");                    
                }   
          }
     }
      else
        JOptionPane.showMessageDialog(null,"Select a row to Update","Error Message",JOptionPane.ERROR_MESSAGE);
     
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
     if(tblRoomGuestOrders.getSelectedRow() != -1)
     {     
        int msg = JOptionPane.showConfirmDialog(null, "Do you really want to DELETE this record?");
          
            if (msg == 0) 
            {
               obj.delete("room_guest_orders", "Number", obj.getPrimaryKey(),tblRoomGuestOrders);
            }
     }
      else
        JOptionPane.showMessageDialog(null,"Select a row to Delete","Error Message",JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnDeleteMouseClicked

