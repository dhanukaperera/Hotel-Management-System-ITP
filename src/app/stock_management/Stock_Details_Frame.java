    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked

        if (obj.isEmpty(txtPrice, "Price field is empty") == true) {
            if (obj.isEmpty(txtReoder, "ReOrder Field is empty") == true) {
                if (obj.isEmpty(txtQuantity, "Quantity field is empty") == true) {

                    if (obj.isNumber(txtQuantity, "Quantity field should be a number") == true) {
                        if (obj.isNumber(txtPrice, "Price field should be a number") == true) {
                            if (obj.isNumber(txtReoder, "Reorder field Should Be A number") == true) {
                                if (obj.isPositive(txtQuantity, "Quantity field should be positive") == true) {
                                    if (obj.isPositive(txtPrice, "Price field should be positive") == true) {
                                        if (obj.isPositive(txtReoder, "Reorder field Should Be Positive") == true) {
                                            itp.add("stock");

                                            itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
                                            itp2.getRole(cmbEmpId.getSelectedItem().toString());
                                            itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
                                        }
                                    }
                                }
                            }
                        }

                    }
                }
            }
        }


    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked

        if (obj.isEmpty(txtPrice, "Price field is empty") == true) {
            if (obj.isEmpty(txtReoder, "ReOrder field is empty") == true) {
                if (obj.isEmpty(txtQuantity, "Quantity field is empty") == true) {
                    if (obj.isNumber(txtQuantity, "Quantity field should be a number") == true) {
                        if (obj.isNumber(txtPrice, "Price field should be a number") == true) {
                            if (obj.isNumber(txtReoder, "Reorder field Should Be A number") == true) {
                                if (obj.isPositive(txtReoder, "Reorder field Should Be Positive") == true) {
                                    if (obj.isPositive(txtQuantity, "Quantity field should be positive") == true) {
                                        if (obj.isPositive(txtPrice, "Price field should be positive") == true) {

                                            int msg = JOptionPane.showConfirmDialog(null, "Do you really want to Update this record?");

                                            if (msg == 0) {
                                                itp.update("stock");

                                                itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
                                                itp2.getRole(cmbEmpId.getSelectedItem().toString());
                                                itp.loadTable2("suppliers", tblSupplier2);
                                                itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked

        int msg = JOptionPane.showConfirmDialog(null, "Do you really want to DELETE this record?");

        if (msg == 0) {
            itp.delete("stock", "Stock ID", itp.getPrimaryKey(), tblStockDetails);
            itp2.getRole(cmbEmpId.getSelectedItem().toString());
            itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
            itp.loadTable("stock", Stock_Frame_Frame.tblStockFrame);
            // itp2.LoadStockName();
        }


    }//GEN-LAST:event_btnDeleteMouseClicked

