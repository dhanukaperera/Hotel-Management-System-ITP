    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        if (obj.isEmpty(txtFirstName, "First Name field is empty") == true) {
            if (obj.isEmpty(txtLastName, "Last Name field is empty") == true) {
                if (obj.isEmpty(txtCompanyName, "Company Name field is empty") == true) {
                    if (obj.isEmpty(txtAddress, "Address field is empty") == true) {
                        if (obj.isEmpty(txtProductName, "Product Name field is empty") == true) {
                            if (obj.isEmpty(txtPhone, "Email field is empty") == true) {
                                if (obj.isLetters(txtFirstName, "First Name Can Only be Letters")) {
                                    if (obj.isLetters(txtLastName, "Last Name Can Only be Letters")) {
                                        if (obj.isLetters(txtCompanyName, "Company Name Can Only be Letters")) {
                                            if (obj.isLetters(txtProductName, "Product Name Can Only be Letters")) {

                                                if (obj.validatePhoneNumber(txtPhone, "Phone Number Format is Wrong") == true) {
                                                    itp.add("suppliers");
                                                    itp.loadTable("suppliers", tblSupplier);
                                                    itp2.loadTable2("suppliers", Stock_Details_Frame.tblSupplier2);
                                                    itp3.ProductId();
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        int msg = JOptionPane.showConfirmDialog(null, "Do you really want to DELETE this record?");

        if (msg == 0) {
            itp.delete("suppliers", "Product ID", itp.getPrimaryKey(), tblSupplier);
            itp2.loadTable2("suppliers", Stock_Details_Frame.tblSupplier2);
            itp3.ProductId();
        }

    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        if (obj.isEmpty(txtFirstName, "First Name field is empty") == true) {
            if (obj.isEmpty(txtLastName, "Last Name field is empty") == true) {
                if (obj.isEmpty(txtCompanyName, "Company Name field is empty") == true) {
                    if (obj.isEmpty(txtAddress, "Address field is empty") == true) {
                        if (obj.isEmpty(txtProductName, "Product Name field is empty") == true) {
                            if (obj.isEmpty(txtPhone, "Email field is empty") == true) {
                                if (obj.validatePhoneNumber(txtPhone, "Phone Number Format is Wrong") == true) {
                                    int msg = JOptionPane.showConfirmDialog(null, "Do you really want to Update this record?");

                                    if (msg == 0) {
                                        itp.update("suppliers");
                                        itp.loadTable("suppliers", tblSupplier);
                                        itp2.loadTable2("suppliers", Stock_Details_Frame.tblSupplier2);
                                        itp3.ProductId();
                                    }
                                }

                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

